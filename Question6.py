# What is a null and alternative hypothesis? Give some examples.

# Answer - 
# Null Hypothesis (H0) - This Hypothesis consist of a Conclusion which we estimate in the beginning. It is denoted by H0.

# Alternate Hypothesis (H1) - This Hypothesis Consist of opposite conclusion of Null hypothesis. It is denoted by H1.

# For example - we did a toss and need to find whether the toss is fair or not. 

# 1. Null Hypothesis - Coin is fair
# 2. Alternate Hypothesis - Coin is unfair