# Q4. Create a hypothesis that states whether the average weight of male college students is greater than
# the average weight of female college students.

# Answer - 

# Null Hyposthesis (H0) -The Average Weights of Male college students is greater than the average height of female college students.

# Alternate Hyposthesis (H1) -The Average Weights of Male college students is equal to the average height of female college students.

# As we can assumes that the Null hypothesis states that there would be no significance changes between male college students and female college students
# whereas Alternate hypothesis states that weight of male college students would be greater then the female college students.